            yield return new TextBlock { Id = Guid.Parse(""), Type = TextBlockType.Text, Text = "Darkness effects extinguish non-magical light in the area, and can counteract less powerful magical light. You must usually target light magic with your darkness magic directly to counteract the light, but some darkness spells automatically attempt to counteract light." };
